// Примеры использования компонента SectionHeader
// Этот файл содержит только комментарии с примерами кода

// 1. Для Categories секции с ссылкой "All categories"
/*
<SectionHeader 
  title="Categories" 
  linkText="All categories"
  linkTo="/categories"
/>
*/

// 2. Для Sale секции с ссылкой "All sales"
/*
<SectionHeader 
  title="Sale" 
  linkText="All sales"
  linkTo="/sales"
/>
*/

// 3. Для Shopping cart с ссылкой "Back to the store"
/*
<SectionHeader 
  title="Shopping cart" 
  linkText="Back to the store"
  linkTo="/"
/>
*/

// 4. Только заголовок без ссылки
/*
<SectionHeader 
  title="My Products" 
/>
*/

// 5. С кастомным тегом заголовка
/*
<SectionHeader 
  title="Main Categories" 
  titleTag="h1"
  linkText="View all"
  linkTo="/categories"
/>
*/

